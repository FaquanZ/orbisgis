package org.orbisgis.tools.instances;

import java.awt.Point;
import java.awt.geom.Rectangle2D;
import java.net.URL;

import org.orbisgis.tools.ToolManager;
import org.orbisgis.tools.TransitionException;
import org.orbisgis.tools.ViewContext;

/**
 * Tool to zoom in
 *
 * @author Fernando Gonzalez Cortes
 */
public class ZoomInTool extends AbstractRectangleTool {

	@Override
	protected void rectangleDone(Rectangle2D rect,
			boolean smallerThanTolerance, ViewContext vc, ToolManager tm)
			throws TransitionException {
		vc.setExtent(rect);
	}

	@Override
	public URL getMouseCursorURL() {
		return this.getClass().getResource("generated/zoom_in.gif");
	}

	@Override
	public Point getHotSpotOffset() {
		return new Point(5, 5);
	}

	public boolean isEnabled(ViewContext vc, ToolManager tm) {
		return vc.atLeastNThemes(1);
	}

	public boolean isVisible(ViewContext vc, ToolManager tm) {
		return true;
	}

}
