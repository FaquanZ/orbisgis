package org.orbisgis.tools;

public class EditionContextException extends Exception {

	public EditionContextException() {
		super();
	}

	public EditionContextException(String message, Throwable cause) {
		super(message, cause);
	}

	public EditionContextException(String message) {
		super(message);
	}

	public EditionContextException(Throwable cause) {
		super(cause);
	}

}
