	/**
	 * creates a composite symbol of jaxb
	 *
	 * @param com
	 *            the symbol composite
	 * @return Compositesymboltype
	 */
	public Object toXML(Symbol com) {
		ObjectFactory of = new ObjectFactory();

		if (com.acceptsChildren()) {
			SymbolCollection col = of.createSymbolCollection();
			for (int i = 0; i < com.getSymbolCount(); i++) {
				Object xmlSymbol = toXML(com.getSymbol(i));
				if (xmlSymbol != null) {
					col.getSimpleSymbolOrSymbolCollection().add(xmlSymbol);
				}
			}

			return col;
		} else {
			if (com instanceof EditableSymbol) {
				SimpleSymbol ret = of.createSimpleSymbol();
				if (com instanceof EditableLineSymbol) {
					EditableLineSymbol els = (EditableLineSymbol) com;
					Color outlineColor = els.getOutlineColor();
					if (outlineColor != null) {
						ret.setLineColor(outlineColor.getRGB());
					} else {
						ret.setLineColor(null);
					}
					ret.setLineWidth(els.getLineWidth());
				}

				if (com instanceof EditablePolygonSymbol) {
					EditablePolygonSymbol els = (EditablePolygonSymbol) com;
					Color fillColor = els.getFillColor();
					if (fillColor != null) {
						ret.setFillColor(fillColor.getRGB());
					} else {
						ret.setFillColor(null);
					}
				}

				if (com instanceof EditablePointSymbol) {
					EditablePointSymbol els = (EditablePointSymbol) com;
					ret.setSymbolSize(els.getSize());
				}
				ret.setSymbolClassId(((EditableSymbol) com).getId());
				return ret;
			} else {
				return null;
			}
		}
	}

	/**
	 * Saves the collection object in the specified file
	 *
	 * @param coll
	 * @param file
	 * @throws JAXBException
	 * @throws IOException
	 */
	public void saveCollection(SymbolCollection coll) throws JAXBException,
			IOException {
		JAXBContext jaxbContext = JAXBContext.newInstance(
				"org.orbisgis.editorViews.toc.actions.cui.persistence", this
						.getClass().getClassLoader());
		Marshaller m = jaxbContext.createMarshaller();

		BufferedOutputStream os = new BufferedOutputStream(
				new FileOutputStream(getSymbolCollectionFile()));
		m.marshal(coll, os);
		os.close();
	}

	/**
	 * loads a collection object and returns it from the specified file
	 *
	 * @param file
	 * @return SymbolCollection
	 * @throws JAXBException
	 */
	public SymbolCollection loadCollection(FileInputStream file)
			throws JAXBException {
		JAXBContext jaxbContext = JAXBContext.newInstance(
				"org.orbisgis.editorViews.toc.actions.cui.persistence", this
						.getClass().getClassLoader());
		Unmarshaller u = jaxbContext.createUnmarshaller();

		SymbolCollection coll = (SymbolCollection) u.unmarshal(file);
		return coll;
	}

	/**
	 * loads an xml file and creates the panels for the created symbols
	 *
	 * @param fileInputStream
	 * @throws JAXBException
	 */
	private void loadXML(FileInputStream fileInputStream) throws JAXBException {
		// JAXBContext
		// jaxbContext=JAXBContext.newInstance("org.orbisgis.geoview.cui.gui.symbolcollection");
		SymbolCollection coll = loadCollection(fileInputStream);
		Symbol symbolCollection = getSymbolFromXML(coll);
		for (int i = 0; i < symbolCollection.getSymbolCount(); i++) {
			addSymbolToPanel(symbolCollection.getSymbol(i));
		}
	}

	private Symbol getSymbolFromXML(Object sim) {
		if (sim instanceof SimpleSymbol) {
			SimpleSymbol simpleSymbol = (SimpleSymbol) sim;
			String id = simpleSymbol.getSymbolClassId();
			EditableSymbol ret = SymbolEditor.getNewSymbol(id);
			if (ret != null) {
				ret.setName(simpleSymbol.getName());
				if (ret instanceof EditableLineSymbol) {
					EditableLineSymbol lineSym = (EditableLineSymbol) ret;
					lineSym.setLineWidth(simpleSymbol.getLineWidth());
					Integer lineColor = simpleSymbol.getLineColor();
					if (lineColor != null) {
						lineSym.setOutlineColor(new Color(lineColor));
					} else {
						lineSym.setOutlineColor(null);
					}
				}
				if (ret instanceof EditablePolygonSymbol) {
					EditablePolygonSymbol pointSym = (EditablePolygonSymbol) ret;
					Integer fillColor = simpleSymbol.getFillColor();
					if (fillColor != null) {
						pointSym.setFillColor(new Color(fillColor));
					} else {
						pointSym.setFillColor(null);
					}
				}
				if (ret instanceof EditablePointSymbol) {
					EditablePointSymbol pointSym = (EditablePointSymbol) ret;
					pointSym.setSize(simpleSymbol.getSymbolSize());
				}

				return ret;
			} else {
				return null;
			}
		} else {
			SymbolCollection sc = (SymbolCollection) sim;
			ArrayList<Symbol> ret = new ArrayList<Symbol>();
			List<Object> symbols = sc.getSimpleSymbolOrSymbolCollection();
			for (int i = 0; i < symbols.size(); i++) {
				Symbol symbolFromXML = getSymbolFromXML(symbols.get(i));
				if (symbolFromXML != null) {
					ret.add(symbolFromXML);
				}
			}

			return SymbolFactory.createSymbolComposite(ret
					.toArray(new Symbol[0]));
		}
	}

	private void addSymbolToPanel(Symbol sym) {
		SelectableCanvas can = new SelectableCanvas();

		can.setSymbol(sym);
		can.setPreferredSize(new Dimension(126, 70));

		can.addMouseListener(new java.awt.event.MouseAdapter() {
			public void mouseClicked(java.awt.event.MouseEvent evt) {
				jPanelPreviewSymbolsMouseClicked(evt);
			}
		});

		jPanelPreviewSymbols.add(can);
	}

