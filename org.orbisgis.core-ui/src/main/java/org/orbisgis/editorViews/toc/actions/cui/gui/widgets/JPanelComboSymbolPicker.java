	private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jComboBox1ActionPerformed
		String sel = (String) jComboBox1.getSelectedItem();
		if (sel == "Simple symbol") {
			updateEnabled(true);
		} else {
			updateEnabled(false);
		}

		jTextField1.setText((String) jComboBox1.getSelectedItem() + " -- "
				+ (new Random()).nextInt());
	}// GEN-LAST:event_jComboBox1ActionPerformed

	private void updateEnabled(boolean b) {
		jRadioButton1.setEnabled(b);
		jRadioButton2.setEnabled(b);
		jRadioButton3.setEnabled(b);
	}

