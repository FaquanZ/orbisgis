	public String postProcess() {
		if (getGeometryType() == GeometryConstraint.MIXED
				&& jComboBox1.getSelectedItem().equals("Simple symbol")) {
			return "You must select one Geometry Type";
		}
		if (jTextField1.getText().equals("")) {
			return "You must select one name";
		}
		return null;
	}

