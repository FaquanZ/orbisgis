package org.orbisgis.core.ui.editorViews.toc.actions.cui;


public class JSE_Datas {
	
	private float x;
	private float value;
	
	public JSE_Datas(float x, float value) {
		this.x = x;
		this.value = value;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getValue() {
		return value;
	}

	public void setValue(float value) {
		this.value = value;
	}
	
	
	

}
