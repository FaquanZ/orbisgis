package org.orbisgis.core.ui.editorViews.toc.actions.cui.type;

import org.orbisgis.core.ui.editorViews.toc.actions.cui.LegendUIComponent;
import org.orbisgis.core.ui.editorViews.toc.actions.cui.LegendUIController;

/**
 *
 * @author maxence
 */
public abstract class LegendUIType {
	protected final LegendUIController controller;
	protected final String name;

	public LegendUIType(String name, LegendUIController controller){
		this.controller = controller;
		this.name = name;
	}

	@Override
	public final String toString(){
		return name;
	}

	public abstract LegendUIComponent getUIComponent(LegendUIComponent parent);
}
